# Membership Operator

a = [1, 2, 3, 4, 5]

print(1 in a) # True
print(6 in a) # False
print(1 not in a) # False
print(6 not in a) # True

b = "Hello"

print("H" in b) # True
print("h" in b) # False
print("H" not in b) # False